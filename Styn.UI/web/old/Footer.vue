export default {
  name: "Footer",
  mounted() {
    document.querySelector(".scroll-to-top").addEventListener("click", (e) => {
      e.preventDefault();
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  
    // Mostrar u ocultar el botón según el desplazamiento
    window.addEventListener("scroll", this.handleScroll);
  },
  beforeUnmount() {
    // Remover el evento al desmontar el componente
    window.removeEventListener("scroll", this.handleScroll);
  },
  methods: {
    handleScroll() {
      const scrollButton = document.querySelector(".scroll-to-top");
      if (window.scrollY > 300) {
        scrollButton.classList.add("show-scroll");
      } else {
        scrollButton.classList.remove("show-scroll");
      }
    },
    scrollToTop() {
      window.scrollTo({ top: 0, behavior: "smooth" });
    },
  },
};


